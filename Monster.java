public class Monster
{
	private int health;
	private int armor;
	private int strength;
	
	public void setHealth(int mhealth)
	{
		health = mhealth;
	}
	
	public int getHealth()
	{
		return health;
	}
	
	public void setArmor(int marmor)
	{
		armor = marmor;
	}
	
	public int getArmor()
	{
		return armor;
	}
	
	public void setStrength(int mstrength)
	{
		strength = mstrength;
	}
	
	public int getStrength()
	{
		return strength;
	}
	
}